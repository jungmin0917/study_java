package ex2_animal;

public class Animal {
	private int eye = 2;
	private int leg = 4;
	
	// 기본 생성자
	public Animal() {;}
	
	// getter, setter
	public int getEye() {
		return eye;
	}
	
	public void setEye(int eye) {
		this.eye = eye;
	}
	public int getLeg() {
		return leg;
	}
	public void setLeg(int leg) {
		this.leg = leg;
	}
	
	
}
